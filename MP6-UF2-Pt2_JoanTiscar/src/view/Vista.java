    public void displayResult(List resultList) {
        Vector<String> tableHeaders = new Vector<String>();
        Vector tableData = new Vector();
        tableHeaders.add("Nom");
        tableHeaders.add("Genere");
        tableHeaders.add("Data");

        for (Object o : resultList) {
            Canciones cancion = (Canciones) o;
            Vector<Object> oneRow = new Vector<>();
            oneRow.add(cancion.get_1_Nom());
            oneRow.add(cancion.get_2_Genere());
            oneRow.add(cancion.get_3_Data());
            tableData.add(oneRow);
        }
        resultTable.setModel(new DefaultTableModel(tableData, tableHeaders));

    }

